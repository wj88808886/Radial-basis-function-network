import java.util.*;

public class Base {

	private double x;
	private double weight;
	
	
	public Base(double weight){
		this.weight = weight;
	}
	
	public void setx(double x){
		this.x = x;
	}
	
	public double x(){
		return x;
	}
	
	public void setweight(double weight){
		this.weight = weight;
	}
	
	public double weight(){
		return weight;
	}
	
}
